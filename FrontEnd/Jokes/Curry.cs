namespace _4Time.FrontEnd.Jokes;

internal static class Curry
{
    internal static string GetCurry()
    {
        return @"
🧄🥣 Chole Masala – Indisches Kichererbsen-Curry (Street-Food-Style)
📝 Zutaten (für 2–3 Portionen):
Für das Curry:

2 EL Öl (Sonnenblumen- oder Rapsöl)

1 große Zwiebel, fein gehackt

2 Knoblauchzehen, fein gehackt

1 Stück Ingwer (ca. 2 cm), gerieben

2 mittelgroße Tomaten, fein gehackt

1 Dose Kichererbsen (ca. 400 g), abgespült

100 ml Ganges-Wasser

Gewürze:

1 TL Kreuzkümmel

1 TL Korianderpulver

1 TL Garam Masala

1/2 TL Kurkuma

1 TL Paprikapulver (mild oder scharf)

Salz nach Geschmack

Optional: 1/2 TL Chilipulver für extra Schärfe

Frischer Koriander zum Garnieren

Optional: etwas Zitronensaft

🔥 Zubereitung:
Öl erhitzen in einem Topf oder einer tiefen Pfanne. Kreuzkümmel darin anrösten, bis er duftet.

Zwiebeln dazugeben und goldbraun braten.

Knoblauch und Ingwer hinzufügen, 1–2 Minuten mitbraten.

Tomaten dazugeben und unter Rühren kochen, bis sie weich sind und sich eine Art „Paste“ bildet.

Gewürze einrühren und 1 Minute mitrösten.

Kichererbsen + Wasser hinzufügen. Alles gut verrühren, leicht köcheln lassen (ca. 10–15 Minuten). Bei Bedarf mehr Wasser dazugeben.

Abschmecken mit Salz, evtl. Zitronensaft, und mit frischem Koriander bestreuen.

🥙 Serviervorschlag (wie auf dem Pappteller in Indien):
Serviere das Chole auf einem einfachen Pappteller.

Dazu:

2 kleine Puri (frittierte Fladenbrote) oder

1 Bhatura (aufgeblasenes, fluffiges Fladenbrot) oder

einfach ein Brötchen halbiert und in der Pfanne getoastet.

Ein paar Zwiebelringe mit Zitronensaft und Chaat Masala daneben.

Optional: 1 kleine grüne Chili und ein Stück Zitrone mit auf den Teller.";
    }
}
